# This circle module has function that perform
# calculations related to circles.

import math

# This area function accepts a circle's radius as an
# argument and returns the area of the circle.
def area(radius):
    return math.pi * radius**2

# The circumfrenece function accepts a circle's radius as
# an argument and returns the area of the circle.
def circumference(radius):
    return 2 * math.pi * radius